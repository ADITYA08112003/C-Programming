// Program 13
#include<stdio.h>
int main(){
    int a=20,b=10;
    // Bitwise And(&)
    printf("Bitwise And of a,b is:%d\n",a&b);
    // Bitwise OR(|)
    printf("Bitwise OR of a,b is:%d\n",a|b);
    // Bitwise XOR(^)
    printf("Bitwise XOR of a,b is:%d",a^b);
    // Left Shift (<<)
    printf("Left Shift of a by 2 is:%d\n",a<<2);
    // Right Shift(>>)
    printf("Right Shift of a by 4 is:%d\n",a>>4);
}